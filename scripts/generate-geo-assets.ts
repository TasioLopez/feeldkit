const ADDRESS_TEMPLATES: Record<
  string,
  {
    required_fields: string[];
    field_order: string[];
    postal_code_position: string;
    region_required: boolean;
  }
> = {
  NL: {
    required_fields: ["street", "house_number", "postal_code", "city", "country"],
    field_order: ["street", "house_number", "postal_code", "city", "country"],
    postal_code_position: "before_city",
    region_required: false,
  },
  US: {
    required_fields: ["street", "city", "state", "postal_code", "country"],
    field_order: ["street", "city", "state", "postal_code", "country"],
    postal_code_position: "before_country",
    region_required: true,
  },
  CA: {
    required_fields: ["street", "city", "province", "postal_code", "country"],
    field_order: ["street", "city", "province", "postal_code", "country"],
    postal_code_position: "before_country",
    region_required: true,
  },
  DE: {
    required_fields: ["street", "postal_code", "city", "country"],
    field_order: ["street", "postal_code", "city", "country"],
    postal_code_position: "before_city",
    region_required: false,
  },
  FR: {
    required_fields: ["street", "postal_code", "city", "country"],
    field_order: ["street", "postal_code", "city", "country"],
    postal_code_position: "before_city",
    region_required: false,
  },
  GB: {
    required_fields: ["street", "city", "postal_code", "country"],
    field_order: ["street", "city", "postal_code", "country"],
    postal_code_position: "after_city",
    region_required: false,
  },
};

async function main() {
  process.env.FEELDKIT_SOURCE_FORCE_SNAPSHOTS = "1";
  const geoPacks = await buildGeoPacks();
  const merged = mergePacks(geoPacks);
  const geo = merged.get("geo");
  if (!geo) throw new Error("mergePacks: missing geo");

  const countries = geo.fieldTypes.find((ft) => ft.key === "countries")?.values ?? [];
  const subdivisions = geo.fieldTypes.find((ft) => ft.key === "subdivisions")?.values ?? [];
  const region_groups = geo.fieldTypes.find((ft) => ft.key === "geo_region_groups")?.values ?? [];
  const continents = geo.fieldTypes.find((ft) => ft.key === "geo_continents")?.values ?? [];
  const calling_codes = buildE164CallingCodeSeedValues();

  const bundle: GeoBundle = {
    version: 1,
    generated_at: new Date().toISOString(),
    countries,
    subdivisions,
    region_groups,
    continents,
    calling_codes,
  };

  const outDir = resolve(process.cwd(), "src/data/generated");
  await mkdir(outDir, { recursive: true });

  await writeFile(resolve(outDir, "geo-bundle.json"), JSON.stringify(bundle, null, 2), "utf8");

  const postalTs = `/* eslint-disable */\n/** Auto-generated by scripts/generate-geo-assets.ts — run npm run generate:geo */\nexport const POSTAL_PATTERN_SOURCE: Record<string, string> = ${JSON.stringify(POSTAL_PATTERN_SOURCE, null, 2)};\n`;
  await writeFile(resolve(outDir, "postal-patterns.ts"), postalTs, "utf8");

  const addrTs = `/* eslint-disable */\n/** Auto-generated by scripts/generate-geo-assets.ts */\nexport type AddressTemplateV1 = {\n  required_fields: string[];\n  field_order: string[];\n  postal_code_position: string;\n  region_required: boolean;\n};\n\nexport const ADDRESS_TEMPLATES_BY_ISO2: Record<string, AddressTemplateV1> = ${JSON.stringify(ADDRESS_TEMPLATES, null, 2)};\n`;
  await writeFile(resolve(outDir, "address-templates.ts"), addrTs, "utf8");

  console.log(
    `generate:geo wrote geo-bundle.json (${countries.length} countries, ${subdivisions.length} subdivisions, ${region_groups.length} region groups, ${continents.length} continents, ${calling_codes.length} calling codes), postal-patterns.ts, address-templates.ts`,
  );
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
