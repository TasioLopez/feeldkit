/**
 * Regenerates committed geo assets for static `v1` and validation helpers.
 *
 * Usage (repo root): `npm run generate:geo`
 */
import { mkdir, writeFile } from "node:fs/promises";
import { resolve } from "node:path";
import { buildE164CallingCodeSeedValues } from "../src/lib/geo/telephony-seed-values";
import { mergePacks } from "../src/lib/ingestion/ingest-seed-bundle";
import { buildGeoPacks } from "./sources/geo-build";

type GeoBundle = {
  version: 1;
  generated_at: string;
  countries: Array<{ key: string; label: string; aliases?: string[]; metadata?: Record<string, unknown> }>;
  subdivisions: Array<{ key: string; label: string; aliases?: string[]; metadata?: Record<string, unknown> }>;
  region_groups: Array<{ key: string; label: string; aliases?: string[]; metadata?: Record<string, unknown> }>;
  continents: Array<{ key: string; label: string; aliases?: string[]; metadata?: Record<string, unknown> }>;
  calling_codes: Array<{ key: string; label: string; aliases?: string[]; metadata?: Record<string, unknown> }>;
};

